
public class JavaPjt4 {

	public static void main(String[] args) {
		
		
		/*
		 * 복합대입연산자
		 * x += 10 은 x = x + 10 랑 같은 의미
		 */
		int x = 10;
		System.out.println("x += 10 : " + (x += 10));
		
		//관계연산자
		// != a!=b a와 b가 같지 않으면 참
		int y = 20;
		System.out.println("x==y: " + (x==y));
		System.out.println("x!=y: " + (x!=y));
		
		
		
	}
	
	
	
}
